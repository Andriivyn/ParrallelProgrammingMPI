public class CollectiveFunctions_Task3 {
    public static void main(String[] args) throws MPIException {

        MPI.Init(args);

        int myRank = MPI.COMM_WORLD.getRank();
        int np = MPI.COMM_WORLD.getSize();

        int n = (myRank + 1) * 5;
        int[] a = new int[n];

        for (int i = 0; i < n; i++) a[i] = myRank * 10 + i;

        System.out.println("myRank = " + myRank + " : a = " + Arrays.toString(a));

        int[] q = new int[5 * ((1 + np) * np) / 2];

        int[] sizesArray = new int[np];    // array of sizes of arrays
        int[] displaysArray = new int[np]; // array of indents

        for (int i = 0; i < np; i++) {
            sizesArray[i] = 5 * (i + 1);

            if (i == 0) displaysArray[i] = 0;
            else displaysArray[i] = displaysArray[i - 1] + i * 5;
        }

        MPI.COMM_WORLD.gatherv(a, n, MPI.INT, q, sizesArray, displaysArray, MPI.INT, 3);

        if(myRank == 3) System.out.println("myRank = " + myRank + " : q = " + Arrays.toString(q));

        MPI.Finalize();

    }
}

